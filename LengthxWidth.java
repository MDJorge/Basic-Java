public class LengthxWidth
{
	public static void main(String[] args)
	{
		int length = 24, area = 121;
		int width = area / length;
		
		// print the statement inside the parentheses to the console.
		System.out.println("The area is " + area + " and the length is " + length + ".");
		System.out.println("The integer width is " + width + ".");
		System.out.print("length x width = " + (length * width) + ".");
	}	
}